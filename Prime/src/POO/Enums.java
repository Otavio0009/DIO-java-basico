package POO;

public interface Enums {
    /*Enum é um tipo especial de classe onde os objetos são previamente criados, imutáveis e disponível por toda aplicaçõ.
    
    Usamos Enum quando o nosso modelo do negócio contém objetos de mesmo contexto que já existem pré-estabelecida com a certeza de
    não haver alteraçaõ de valores.
    
    Exemplos:
    Grau de Escolaridade: Analfabeto, Fundamental, Médio e Superior.
    Estado Civil: Solteiro, Casado, Divorciado, Viúvo.
    Estados Brasileiros: Praiba, Pernabuco, Bhaia, Piauí e Rio Grabde do NOrte.
    
    Não confuda uma lista de constantes com um enum.
    
    Enquanto que uma constate é uma variável de tipo com valoer imutável, um enum é uma variavel de ojetos já pre-definidos na aplicação.
    
    Como um enum é um conjuto de objetos, logo, estes objetos podem conter atributos e métodos.
    Veja o exemplo de um enume para disponibilizar os quatro estsdos brsileiros citados acima, contendo informações:
    Nome, Sigla e um método que paga o nome de cada estado e já retorna para tudo maiúsculo.
     */
}
