package POO;

public interface Classes {
    /* Toda a estrutura de código na liguangem java é distribuida em arquivos com extensão ".java" denominados de classe.
    
    AS classes existentes em nosso projeto serão composta por:
    
    Identificador, Características e Comportamentos.
    Classe (class): A estrutura e ou representação que direciona a criação dos objetos de mesmo tipo.
    Identificador (identity): Propósito existencialaos objetos que serão criados.
    Características (states): Támbem conhecido como atributos ou propriedades, é toda informação que representa o estado do objeto.
    Componetes (behavio): Também conhecido como ação ou métodos, é toda parte comportamental que um objeto dispõe.
    Instancia (new): é o ato de criar um objeto a partir de estrutura definida em uma classe.
    
    Seguindo algumas convenções, as nossas classes são classificadas como:
    
    Classe de modelo (model): Classes que representem estruturas de domínio
    da aplicação, exemplo. Cliente, Pedido, Nota FIscal e etc.
    
    Classe de serviço (service): Classes que contém regras de negócio e validação de nosso sistema.
    
    Classe de repositório (repository): CXlassses que contém um integrção com bamco de dados.
    
    Classe decontrole (controller): Classes que possuem a finalidade de disponibilidade alguma comunicação externa à nossa aplicação,
    tipo http web ou webservices.
    
    Classe utilitaria (utill): Classe que contém recursos comuns à toda nossa aplicação.
    */
}
